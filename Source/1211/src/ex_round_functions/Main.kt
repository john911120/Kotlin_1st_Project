package ex_round_functions

import kotlin.math.*

fun main()
{
    print("${ceil(3.2)} ")
    print("${ceil(4.71)} ")
    print("${floor(3.2)}")
    print("${floor(4.71)}")
    print("${round(3.4)}")
    print("${round(7.41)}")
    print("${12.34.roundToInt()}")
    print("${23.34.roundToLong()}")
}